class Solution {
  public:
    void printTriangle(int n) {
        // code here
        for(int r=n; r>=1; r--)
        {
            
            for(int c1=n; c1>=r; c1--)
            cout<<(char)(c1+64)<<" ";
            
            cout<<endl;
        }
    }
};

//{ Driver Code Starts.

int main() {
    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;

        Solution ob;
        ob.printTriangle(n);
    }
    return 0;
}
